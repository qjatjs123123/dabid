create table random_nickname (
   id int auto_increment primary key,
   nickname varchar(20),
   used tinyint
);

insert into random_nickname (nickname, used)
select concat(a, ' ', s), 0 from adj, snack order by rand();

# 사용되지 않은 닉네임 찾기
select nickname from random_nickname
where id = (select min(id) from random_nickname where used = false);